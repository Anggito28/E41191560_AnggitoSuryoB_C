    private void carinamaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_carinamaActionPerformed
       try {
            Statement stt = con.createStatement();
            ResultSet rss = stt.executeQuery("Select * from tb_karyawan" + " where nama_pegawai like('%" +nmkaryawan.getText() + "%')");

            if (rss.next()) {
                nmkaryawan.setText(rss.getString("nama_pegawai"));
                jnskelamin.setSelectedItem(rss.getString("kelamin"));
                alamatteks.setText(rss.getString("alamat"));
                username.setText(rss.getString("User_ID"));
                password.setText(rss.getString("Pass_ID"));
                jabatan.setSelectedItem(rss.getString("Jabatan"));
                kdkaryawan.setText(rss.getString("kode_pegawai"));
            } else {
                JOptionPane.showMessageDialog(this,"Nama: Salah", "Informasi",
                        JOptionPane.INFORMATION_MESSAGE);

                nmkaryawan.setText("");
                jnskelamin.setSelectedItem("Laki-laki"); 
                alamatteks.setText("");
                username.setText("");
                password.setText("");
                jabatan.setSelectedItem("admin");
                kdkaryawan.setText("");
                nmkaryawan.requestFocus();
            }
        }
           catch (SQLException e){
            System.out.println("koneksi gagal " + e.toString());
           }
    }//GEN-LAST:event_carinamaActionPerformed

