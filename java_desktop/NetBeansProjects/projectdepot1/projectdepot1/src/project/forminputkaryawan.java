    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        String kode_pegawai = kdkaryawan.getText();
        String nama_pegawai = nmkaryawan.getText();
        String kelamin1 = (String)jnskelamin.getSelectedItem();
        String kelamin = null;
        switch (kelamin1) {
            case"Laki-laki":
                kelamin ="Laki-laki";
                break;
            case"Perempuan":
                kelamin ="Perempuan";
                break;
        }
        String alamat = alamatteks.getText();
        String User_ID = username.getText();
        String Pass_ID = password.getText();
        String Jabatan1 = (String)jabatan.getSelectedItem();
        String Jabatan = null;
        switch (Jabatan1){
            case"admin":
                Jabatan ="admin";
                break;
            case"pegawai":
                Jabatan ="pegawai";
                break;
        }
        try {
            query ="insert into tb_karyawan values('"+kode_pegawai+"','"+nama_pegawai+"','"+kelamin+"','"+alamat+"','"+User_ID+"','"+Pass_ID+"','"+Jabatan+"')";
            stt.executeUpdate(query);
            JOptionPane.showMessageDialog(this, "Sudah Berhasil","Success",JOptionPane.INFORMATION_MESSAGE);
            reset();
        }catch(Exception e){
            JOptionPane.showMessageDialog(this,"Masukkan Data Kembali"+ e.getMessage(),"Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_saveActionPerformed

    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
        try {
            PreparedStatement pStatement = con.prepareStatement("DELETE FROM tb_karyawan WHERE kode_pegawai=?");

            pStatement.setString(1,kdkaryawan.getText());

            if (pStatement.executeUpdate()>0)
                JOptionPane.showMessageDialog
               (this,"Delete sukses", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            else
                JOptionPane.showMessageDialog
                (this, "Delete gagal", "Informasi", JOptionPane.INFORMATION_MESSAGE);

            pStatement.close();

            nmkaryawan.setText("");
            jnskelamin.setSelectedItem("Laki-laki"); 
            alamatteks.setText("");
            username.setText("");
            password.setText("");
            jabatan.setSelectedItem("admin");
            kdkaryawan.setText("");
        }
        catch (SQLException e){
            System.out.println("koneksi gagal " + e.toString());
        }
    }//GEN-LAST:event_DeleteActionPerformed

