    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int row = jTable1.getSelectedRow();
        kodemenu.setText(jTable1.getModel().getValueAt(row,0).toString());
        namamenu.setText(jTable1.getModel().getValueAt(row,1).toString());
        stokmenu.setText(jTable1.getModel().getValueAt(row,2).toString());
        hargamenu.setText(jTable1.getModel().getValueAt(row,3).toString());
        String katagori = jTable1.getModel().getValueAt(row,4).toString();
        ambilkatagori(katagori);
    }//GEN-LAST:event_jTable1MouseClicked

