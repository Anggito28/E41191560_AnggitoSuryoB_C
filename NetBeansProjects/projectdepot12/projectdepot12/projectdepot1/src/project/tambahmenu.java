    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
      try {
            PreparedStatement pStatement = con.prepareStatement("UPDATE tb_menu SET nama_makanan=?,stok_makanan=?,harga_makanan=?,kode_katagori=? WHERE kode_menu=?");

            pStatement.setString(1, namamenu.getText());
            pStatement.setString(2, stokmenu.getText());
            pStatement.setString(3, hargamenu.getText());
            pStatement.setString(4, (String) katmenu.getSelectedItem());
            pStatement.setString(5, kodemenu.getText());
            
            if ( pStatement.executeUpdate()>0)
                JOptionPane.showMessageDialog(this,"Editsukses", "Informasi", JOptionPane.INFORMATION_MESSAGE);
            else
                JOptionPane.showMessageDialog(this, "Edit gagal",
                "Informasi", JOptionPane.INFORMATION_MESSAGE);

            pStatement.close();

            namamenu.setText("");
            stokmenu.setText("");
            hargamenu.setText("");
            katmenu.setSelectedItem("makanan");
            kodemenu.setText("");
        }
            catch (SQLException e){
            System.out.println("koneksi gagal " + e.toString());
        }
      ViewData();
    }//GEN-LAST:event_editActionPerformed

    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        inputmenubar b = new inputmenubar();
        b.kode_menu = kodemenu.getText();
        b.nama_makanan = namamenu.getText();
        b.stok_makanan = stokmenu.getText();
        b.harga_makanan = hargamenu.getText();
        String katagori= (String) katmenu.getSelectedItem();
        b.kode_katagori = null;
        switch (katagori){
            case"makanan":
                b.kode_katagori ="KTG01";
                break;
            case"minuman":
                b.kode_katagori ="KTG02";
                break;
            case"cemilan":
                b.kode_katagori ="KTG03";
                break;
        }
        try{
            b.simpan();
            JOptionPane.showMessageDialog(null,"berhasil di upload");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        ViewData();
    }//GEN-LAST:event_saveActionPerformed

